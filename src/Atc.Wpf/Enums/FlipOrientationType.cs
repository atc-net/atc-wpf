namespace Atc.Wpf.Controls;

/// <summary>
/// Defines the different flip orientations that a icon can have.
/// </summary>
public enum FlipOrientationType
{
    /// <summary>
    /// Default
    /// </summary>
    Normal = 0,

    /// <summary>
    /// Flip horizontally (on x-achsis)
    /// </summary>
    Horizontal,

    /// <summary>
    /// Flip vertically (on y-achsis)
    /// </summary>
    Vertical,
}