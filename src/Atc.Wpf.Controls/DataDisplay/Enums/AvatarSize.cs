namespace Atc.Wpf.Controls;

/// <summary>
/// Specifies the size preset for an Avatar control.
/// </summary>
public enum AvatarSize
{
    /// <summary>
    /// Extra small avatar (24px).
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Small avatar (32px).
    /// </summary>
    Small,

    /// <summary>
    /// Medium avatar (40px). This is the default size.
    /// </summary>
    Medium,

    /// <summary>
    /// Large avatar (56px).
    /// </summary>
    Large,

    /// <summary>
    /// Extra large avatar (80px).
    /// </summary>
    ExtraLarge,
}