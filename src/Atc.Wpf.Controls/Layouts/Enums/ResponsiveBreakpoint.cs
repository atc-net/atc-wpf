namespace Atc.Wpf.Controls;

/// <summary>
/// Specifies the responsive breakpoint tiers for ResponsivePanel.
/// </summary>
public enum ResponsiveBreakpoint
{
    /// <summary>
    /// Extra small screens (less than 576px).
    /// </summary>
    Xs,

    /// <summary>
    /// Small screens (576px to 767px).
    /// </summary>
    Sm,

    /// <summary>
    /// Medium screens (768px to 991px).
    /// </summary>
    Md,

    /// <summary>
    /// Large screens (992px to 1199px).
    /// </summary>
    Lg,

    /// <summary>
    /// Extra large screens (1200px and above).
    /// </summary>
    Xl,
}
