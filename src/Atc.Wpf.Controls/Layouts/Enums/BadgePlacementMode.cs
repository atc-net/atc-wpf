namespace Atc.Wpf.Controls;

/// <summary>
/// Specifies the placement of the badge relative to the content.
/// </summary>
public enum BadgePlacementMode
{
    /// <summary>
    /// Badge is placed at the top-left corner.
    /// </summary>
    TopLeft,

    /// <summary>
    /// Badge is placed at the top center.
    /// </summary>
    Top,

    /// <summary>
    /// Badge is placed at the top-right corner.
    /// </summary>
    TopRight,

    /// <summary>
    /// Badge is placed at the right center.
    /// </summary>
    Right,

    /// <summary>
    /// Badge is placed at the bottom-right corner.
    /// </summary>
    BottomRight,

    /// <summary>
    /// Badge is placed at the bottom center.
    /// </summary>
    Bottom,

    /// <summary>
    /// Badge is placed at the bottom-left corner.
    /// </summary>
    BottomLeft,

    /// <summary>
    /// Badge is placed at the left center.
    /// </summary>
    Left,
}