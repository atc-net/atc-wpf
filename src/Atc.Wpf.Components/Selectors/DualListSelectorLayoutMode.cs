namespace Atc.Wpf.Components.Selectors;

/// <summary>
/// Specifies the layout mode for a <see cref="DualListSelector"/> control.
/// </summary>
public enum DualListSelectorLayoutMode
{
    /// <summary>
    /// Available list on the left, Selected list on the right.
    /// </summary>
    AvailableFirst,

    /// <summary>
    /// Selected list on the left, Available list on the right.
    /// </summary>
    SelectedFirst,
}