namespace Atc.Wpf.Tests.UndoRedo;

public sealed class RichPropertyChangeCommandTests
{
    [Fact]
    public void Execute_SetsNewValue()
    {
        var value = "old";
        var command = new RichPropertyChangeCommand<string>(
            "test",
            v => value = v,
            "old",
            "new");

        command.Execute();

        value.Should().Be("new");
    }

    [Fact]
    public void UnExecute_RestoresOldValue()
    {
        var value = "new";
        var command = new RichPropertyChangeCommand<string>(
            "test",
            v => value = v,
            "old",
            "new");

        command.UnExecute();

        value.Should().Be("old");
    }

    [Fact]
    public void Properties_RoundTrip()
    {
        var id = Guid.NewGuid();
        var image = "icon.png";
        var parameter = "param";
        var data = 42;

        var command = new RichPropertyChangeCommand<int>(
            "desc",
            _ => { },
            0,
            1,
            id: id,
            image: image,
            parameter: parameter,
            data: data,
            allowUserAction: false);

        command.Description.Should().Be("desc");
        command.Id.Should().Be(id);
        command.Image.Should().Be(image);
        command.Parameter.Should().Be(parameter);
        command.Data.Should().Be(data);
        command.AllowUserAction.Should().BeFalse();
    }

    [Fact]
    public void Id_AutoGenerated_WhenNull()
    {
        var command = new RichPropertyChangeCommand<int>("test", _ => { }, 0, 1);

        command.Id.Should().NotBe(Guid.Empty);
    }

    [Fact]
    public void AllowUserAction_DefaultsToTrue()
    {
        var command = new RichPropertyChangeCommand<int>("test", _ => { }, 0, 1);

        command.AllowUserAction.Should().BeTrue();
    }

    [Fact]
    public void NullDescription_Throws()
    {
        var act = () => new RichPropertyChangeCommand<int>(null!, _ => { }, 0, 1);

        act.Should().Throw<ArgumentNullException>();
    }

    [Fact]
    public void NullSetter_Throws()
    {
        var act = () => new RichPropertyChangeCommand<int>("test", null!, 0, 1);

        act.Should().Throw<ArgumentNullException>();
    }
}